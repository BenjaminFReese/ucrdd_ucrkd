## Initializing Varibales for local linear regression loop
models <- list()
variables <- setdiff(names(f), c("row_id"))
View(variables)

## Local Linear Regression Loop
for (var in variables) {
  models[[var]] = RDestimate(
    as.formula(paste0("y ~ ", var)),
    data = f
  )
}


